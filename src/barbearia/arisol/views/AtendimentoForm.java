    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
//            String data = this.txtData.getText();
            Cliente cliente = (Cliente) this.cbClientes.getSelectedItem();
            Corte corte = (Corte) this.cbCortes.getSelectedItem();
            
            Atendimento a = new Atendimento(new Date(), valorAux, new LoggerDao().getActiveUser(), corte, cliente);
            a.setAgenda(false);
            atendimentoDao.create(a);
            
            GenericMessage.showSuccess(this);
            clear();
            listTable();
        } catch (Exception ex) {
            ex.printStackTrace();
            GenericMessage.showAlertMessage(this, "Falha ao registrar");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

