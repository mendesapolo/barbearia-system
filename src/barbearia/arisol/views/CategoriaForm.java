    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String nome = this.txtNome.getText();
        String descricao = this.txtDescricao.getText();
        Categoria c = new Categoria(nome, descricao, false);
        this.categoriaDao.create(c);
        GenericMessage.showSuccess(this);
        this.listTable();
        this.clean();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tbCategoriaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCategoriaMouseClicked
        Categoria c = new CategoriaAdapter().getRow(this.tbCategoria.getSelectedRow());
        this.txtNome.setText(c.getNome());
        this.txtDescricao.setText(c.getDescricao());
        this.btnSave.setEnabled(false);
        this.btnUpdate.setEnabled(true);
        this.btnDelete.setEnabled(true);
    }//GEN-LAST:event_tbCategoriaMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int idx = this.tbCategoria.getSelectedRow();
        Categoria c = new CategoriaAdapter().getRow(idx);
        if(idx >= 0){
            String nome = this.txtNome.getText();
            String descricao = this.txtDescricao.getText();
        
            if(nome != null && nome != ""){
                c.setNome(nome);
                c.setDescricao(descricao);
                this.categoriaDao.update(c);
                GenericMessage.showSuccessUpdate(this);
                this.listTable();
                this.clean();
            }else{
                GenericMessage.requiredData(this, "Nome");
            }
        }else{
            GenericMessage.dataNotFound(this);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        this.clean();
    }//GEN-LAST:event_btnClearActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int idx = this.tbCategoria.getSelectedRow();
        if(idx >= 0){
            Categoria c = new CategoriaAdapter().getRow(idx);
            if(GenericMessage.confirmDeleteData(this) == JOptionPane.OK_OPTION){
                this.categoriaDao.delete(c);
                this.listTable();
                this.clean();
            }
        }else{
            GenericMessage.dataNotFound(this);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    public void clean(){
        this.txtNome.setText("");
        this.txtDescricao.setText("");
        
        this.btnSave.setEnabled(true);
        this.btnUpdate.setEnabled(false);
        this.btnDelete.setEnabled(false);
    }
    
